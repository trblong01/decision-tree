package com.ssi.decisionrule.utils;

/**
 * @author hieunt32
 */
public class DBConnectionConfig {

    int type;
    String path, key;

    public DBConnectionConfig(int type, String path, String key) {
        this.type = type;
        this.path = path;
        this.key = key;
    }

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getPath() {
        return path;
    }

    public void setPath(String path) {
        this.path = path;
    }
}
